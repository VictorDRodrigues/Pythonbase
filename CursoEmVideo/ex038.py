print('Programa de Avaliação de Comparação!!!')
n1 = float(input('Digite o 1ª valor qualquer: '))
n2 = float(input('Digite o 2ª valor qualquer: '))


if n1 > n2:
    print('O 1ª valor "{}" é MAIOR.'.format(n1))
elif n2 > n1:
    print('O 2ª valor "{}" é MAIOR.'.format(n2))
else :
    print('O 1ª e o 2ª são IGUAIS')
